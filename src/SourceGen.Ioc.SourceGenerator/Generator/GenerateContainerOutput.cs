                    writer.WriteLine($"(Type t, object k) when t == typeof({kvp.Key.ServiceType}) && Equals(k, {kvp.Key.Key}) => {cached.ResolverMethodName}(),");
                }

                // Fallback in switch default case
                if(container.ResolveIServiceCollection)
