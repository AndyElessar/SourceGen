    /// <summary>
    /// Writes Disposal implementation (IDisposable and IAsyncDisposable).
    /// </summary>
    private static void WriteDisposalImplementation(
        SourceWriter writer,
        ContainerModel container,
        ContainerRegistrationGroups groups)
    {
        writer.WriteLine("#region Disposal");
        writer.WriteLine();

        // IDisposable.Dispose
        writer.WriteLine("public void Dispose()");
        writer.WriteLine("{");
        writer.Indentation++;

        writer.WriteLine("if(Interlocked.Exchange(ref _disposed, 1) != 0) return;");
        writer.WriteLine();

        // Dispose scoped services if this is a scope
        writer.WriteLine("if(!_isRootScope)");
        writer.WriteLine("{");
        writer.Indentation++;

        // Use pre-computed reversed list for disposal
        foreach(var cached in groups.ReversedScopedForDisposal)
        {
            writer.WriteLine($"DisposeService({cached.FieldName});");
        }

        // Dispose imported module scopes (child scopes have their own module scopes)
        foreach(var module in container.ImportedModules)
        {
            var fieldName = GetModuleFieldName(module.Name);
            writer.WriteLine($"{fieldName}.Dispose();");
        }

        writer.WriteLine("return;");

        writer.Indentation--;
        writer.WriteLine("}");
        writer.WriteLine();

        // Use pre-computed reversed list for disposal
        foreach(var cached in groups.ReversedSingletonsForDisposal)
        {
            writer.WriteLine($"DisposeService({cached.FieldName});");
        }

        // Dispose imported modules (root scope)
        foreach(var module in container.ImportedModules)
        {
            var fieldName = GetModuleFieldName(module.Name);
            writer.WriteLine($"{fieldName}.Dispose();");
        }

        writer.Indentation--;
        writer.WriteLine("}");
        writer.WriteLine();

        // IAsyncDisposable.DisposeAsync
        writer.WriteLine("public async ValueTask DisposeAsync()");
        writer.WriteLine("{");
        writer.Indentation++;

        writer.WriteLine("if(Interlocked.Exchange(ref _disposed, 1) != 0) return;");
        writer.WriteLine();

        // Dispose scoped services if this is a scope
        writer.WriteLine("if(!_isRootScope)");
        writer.WriteLine("{");
        writer.Indentation++;

        // Use pre-computed reversed list for disposal
        foreach(var cached in groups.ReversedScopedForDisposal)
        {
            writer.WriteLine($"await DisposeServiceAsync({cached.FieldName});");
        }

        // Dispose imported module scopes (child scopes have their own module scopes)
        foreach(var module in container.ImportedModules)
        {
            var fieldName = GetModuleFieldName(module.Name);
            writer.WriteLine($"await {fieldName}.DisposeAsync();");
        }

        writer.WriteLine("return;");

        writer.Indentation--;
        writer.WriteLine("}");
        writer.WriteLine();

        // Use pre-computed reversed list for disposal
        foreach(var cached in groups.ReversedSingletonsForDisposal)
        {
            writer.WriteLine($"await DisposeServiceAsync({cached.FieldName});");
        }

        // Dispose imported modules (root scope)
        foreach(var module in container.ImportedModules)
        {
            var fieldName = GetModuleFieldName(module.Name);
            writer.WriteLine($"await {fieldName}.DisposeAsync();");
        }

        writer.Indentation--;
        writer.WriteLine("}");
        writer.WriteLine();

        // Helper method for async disposal
        writer.WriteLine("private static async ValueTask DisposeServiceAsync(object? service)");
        writer.WriteLine("{");
        writer.Indentation++;
        writer.WriteLine("if(service is IAsyncDisposable asyncDisposable) await asyncDisposable.DisposeAsync();");
        writer.WriteLine("else if(service is IDisposable disposable) disposable.Dispose();");
        writer.Indentation--;
        writer.WriteLine("}");
        writer.WriteLine();

        // Helper method for sync disposal
        writer.WriteLine("private static void DisposeService(object? service)");
        writer.WriteLine("{");
        writer.Indentation++;
        writer.WriteLine("if(service is IDisposable disposable) disposable.Dispose();");
        writer.Indentation--;
        writer.WriteLine("}");
        writer.WriteLine();

        writer.WriteLine("#endregion");
    }

    /// <summary>
    /// Gets the collection resolver method name for IEnumerable&lt;T&gt; resolution.
    /// </summary>
    private static string GetCollectionResolverMethodName(string serviceType)
    {
        var baseName = GetSafeIdentifier(serviceType);
        return $"GetAll{baseName}";
    }

    /// <summary>
    /// Gets the field name for an imported module.
    /// </summary>
    private static string GetModuleFieldName(string moduleName)
    {
        var baseName = GetSafeIdentifier(moduleName);
        return $"_{char.ToLowerInvariant(baseName[0])}{baseName[1..]}";
    }
